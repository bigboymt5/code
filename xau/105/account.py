def is_valid_account(account, plan=None):
    """계좌 유효성 검증 - 플랜별 타입 지원"""
    try:
        if not account:
            return False
        
        if plan == 'big':
            account_str = str(account).strip().upper()
            return account_str in ALLOWED_ACCOUNTS_BIG
        else:
            account_int = int(str(account).strip())
            return account_int in ALLOWED_ACCOUNTS
            
    except (ValueError, TypeError):
        return False


